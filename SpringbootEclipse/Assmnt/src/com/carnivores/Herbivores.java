package com.carnivores;

public class Herbivores {

	String deer,buffalo,cat;

	public Herbivores() {
		super();
	}

	public String getDeer() {
		return deer;
	}

	public void setDeer(String deer) {
		this.deer = deer;
	}

	public String getBuffalo() {
		return buffalo;
	}

	public void setBuffalo(String buffalo) {
		this.buffalo = buffalo;
	}

	public String getCat() {
		return cat;
	}

	public void setCat(String cat) {
		this.cat = cat;
	}
	
	
	
}
